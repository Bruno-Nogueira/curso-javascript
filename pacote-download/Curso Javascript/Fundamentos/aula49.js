const resultado = nota => nota >= 7 ? 'Aprovado' : 'Reprovado' 
/* nota é o parâmetro
se nota >= 7 for true, vai retornar 'Aprovado' 
se nota >= 7 for false, vai retornar 'Reprovado' */
console.log(resultado(7.1)) // Aprovado

